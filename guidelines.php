            <h3 class="text-lg font-bold text-gov-darkblue mb-4">Pandal Placement</h3>
            <ul class="list-disc pl-5 space-y-2 text-gray-700">
              <li>Pandals must not obstruct public roads, footpaths, or emergency access routes.</li>
              <li>Minimum 3 meters clearance must be maintained from any electrical installations.</li>
              <li>Celebrations must maintain at least 100 meters distance from hospitals, educational institutions, and places of worship unless special permission is obtained.</li>
              <li>Ensure adequate space around the pandal for crowd movement and dispersal.</li>
            </ul>
          </div>
          
          <div class="bg-white p-6 border border-gray-200 rounded-sm shadow-sm">
            <h3 class="text-lg font-bold text-gov-darkblue mb-4">Traffic Considerations</h3>
            <ul class="list-disc pl-5 space-y-2 text-gray-700">
              <li>If the pandal is near a roadway, it must not affect the normal flow of traffic.</li>
              <li>Adequate parking arrangements must be planned for larger celebrations.</li>
              <li>Traffic management plan is mandatory for festivals expecting more than 500 visitors per day.</li>
              <li>Coordinate with local traffic police for larger processions and visarjan routes.</li>
            </ul>
          </div>
          
          <div class="bg-white p-6 border border-gray-200 rounded-sm shadow-sm">
            <h3 class="text-lg font-bold text-gov-darkblue mb-4">Restricted Areas</h3>
            <p class="text-gray-700 mb-4">
              Festival pandals are not permitted in the following locations:
            </p>
            <ul class="list-disc pl-5 space-y-2 text-gray-700">
              <li>Protected heritage sites and monuments.</li>
              <li>Environmentally sensitive zones and protected forests.</li>
              <li>High-security areas without special clearance.</li>
              <li>Areas prone to natural disasters or safety hazards.</li>
              <li>Water bodies and their immediate vicinity (except for designated visarjan points).</li>
