            <h3 class="text-lg font-bold text-gov-darkblue mb-4">Fire Safety</h3>
            <ul class="list-disc pl-5 space-y-2 text-gray-700">
              <li>All pandals must have fire extinguishers appropriate to the size of the structure.</li>
              <li>Minimum 2 fire extinguishers (ABC type) for pandals up to 100 sq. meters.</li>
              <li>Additional fire safety equipment for larger pandals as per fire department guidelines.</li>
              <li>Fire-resistant materials must be used for pandal construction.</li>
              <li>Clear fire exits with proper signage is mandatory.</li>
            </ul>
          </div>
          
          <div class="bg-white p-6 border border-gray-200 rounded-sm shadow-sm">
            <h3 class="text-lg font-bold text-gov-darkblue mb-4">Electrical Safety</h3>
            <ul class="list-disc pl-5 space-y-2 text-gray-700">
              <li>All electrical installations must be done by certified electricians.</li>
              <li>Proper earthing and circuit breakers are mandatory.</li>
              <li>No overhead wiring or exposed cables permitted in public access areas.</li>
              <li>Generator sets must be installed with proper safety measures and sound dampening.</li>
              <li>Rainproof arrangements for all electrical equipment during monsoon season.</li>
            </ul>
          </div>
          
          <div class="bg-white p-6 border border-gray-200 rounded-sm shadow-sm">
            <h3 class="text-lg font-bold text-gov-darkblue mb-4">Crowd Management</h3>
            <ul class="list-disc pl-5 space-y-2 text-gray-700">
              <li>Clearly marked entry and exit points with adequate width.</li>
              <li>Proper crowd management plan for festivals expecting large gatherings.</li>
              <li>First aid arrangements with basic medical supplies.</li>
              <li>Emergency contact numbers displayed prominently.</li>
              <li>Volunteer staff trained in basic first aid and emergency procedures.</li>
            </ul>
          </div>
